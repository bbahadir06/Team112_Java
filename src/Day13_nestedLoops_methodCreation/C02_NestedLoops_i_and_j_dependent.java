package Day13_nestedLoops_methodCreation;

public class C02_NestedLoops_i_and_j_dependent {
    public static void main(String[] args) {

        /*
         *
         * *
         * * *
         * * * *
         */

        for (int j = 1; j <= 4; j++) {
            for (int i = 1; i <= j; i++) {
                System.out.print("* ");
            }
            System.out.println();
        }








    }
}
