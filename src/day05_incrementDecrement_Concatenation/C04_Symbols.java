public class C04_Symbols {

    public static void main(String[] args) {

        int a = 10;

        int b = 20;


        System.out.println(a == b); // false

        int c = 30;
        int d = 30;

        System.out.println(c == d);  // true


        System.out.println(a != b); //true












    }
}
